int		ft_atoi(const char *str)
{
	long	i;
	int		n;

	i = 0;
	while ((*str >= '\t' && *str <= '\r') || *str == 32)
		str++;
	if (*str == '-')
		n = -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str && (*str >= '0' && *str <= '9'))
	{
		i = i * 10;
		i += (int)(*str++ - '0');
	}
	if (n == -1)
		return (-i);
	else
		return (i);
}
