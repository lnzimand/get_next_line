#include "libft.h"

int	ft_putnbr(int n)
{
	char a;
	long i;

	i = n;
	if (i < 0)
	{
		ft_putchar('-');
		i *= -1;
	}
	a = '0';
	if (i > 9)
		ft_putnbr(i / 10);
	a = (i % 10) + 48;
	ft_putchar(a);
	return (i * 10);
}
